
public class TimeCost {

	private long time;
	private int cost;
	
	public TimeCost(long time, int cost) {
		this.time = time;
		this.cost = cost;
	}

	public long getTime() {
		return time;
	}

	public void setTime(long time) {
		this.time = time;
	}

	public int getCost() {
		return cost;
	}

	public void setCost(int cost) {
		this.cost = cost;
	}
	
	
}
